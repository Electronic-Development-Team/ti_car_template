    DL_SPI_init(SPI_INST, (DL_SPI_Config *) &gSPI_config);

    /* Configure Controller mode */
    /*
     * Set the bit rate clock divider to generate the serial output clock
     *     outputBitRate = (spiInputClock) / ((1 + SCR) * 2)
     *     13333333.33 = (80000000)/((1 + 2) * 2)
     */
